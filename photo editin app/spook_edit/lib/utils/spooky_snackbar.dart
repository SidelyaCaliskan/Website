              child: Text(
                message,
                style: GoogleFonts.poppins(
                  fontSize: 14,
                  color: Colors.white,
                  fontWeight: FontWeight.w500,
                ),
              ),
            ),
          ],
        ),
        backgroundColor: AppConstants.candyCornYellow,
        behavior: SnackBarBehavior.floating,
        shape: RoundedRectangleBorder(
